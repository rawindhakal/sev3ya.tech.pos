export const BS_MONTH_NAMES = [
  'Baisakh', 'Jestha', 'Ashadh', 'Shrawan', 'Bhadra', 'Ashwin',
  'Kartik', 'Mangsir', 'Poush', 'Magh', 'Falgun', 'Chaitra',
];

export interface BsDate { year: number; month: number; day: number } // month 1-12

// Convert an AD date to BS by walking days from the BS epoch.
export function adToBs(ad: Date): BsDate {
  const adUtc = Date.UTC(ad.getFullYear(), ad.getMonth(), ad.getDate());
  let days = Math.floor((adUtc - BS_EPOCH_AD) / 86400000);
  if (days < 0) throw new Error('Date before BS 2000 (AD 1943-04-14)');
  let year = BS_START_YEAR;
  let month = 0; // index
  for (let y = 0; y < BS_MONTHS.length; y++) {
    for (let m = 0; m < 12; m++) {
      const dim = BS_MONTHS[y][m];
      if (days < dim) return { year, month: m + 1, day: days + 1 };
      days -= dim;
      month = m;
    }
    year++;
  }
  throw new Error('Date beyond BS table (2090)');
}

// "2083-03-26" (zero-padded, sortable — the IRD/CBMS style with dots is below)
export function formatBs(ad: Date): string {
  const b = adToBs(ad);
  return `${b.year}-${String(b.month).padStart(2, '0')}-${String(b.day).padStart(2, '0')}`;
}

// "26 Ashadh 2083" — human-friendly.
export function formatBsLong(ad: Date): string {
  const b = adToBs(ad);
  return `${b.day} ${BS_MONTH_NAMES[b.month - 1]} ${b.year}`;
}

// IRD/CBMS invoice_date format: "2083.03.26"
export function formatBsIrd(ad: Date): string {
  const b = adToBs(ad);
  return `${b.year}.${String(b.month).padStart(2, '0')}.${String(b.day).padStart(2, '0')}`;
}

// Nepali fiscal year runs Shrawan (month 4) → Ashadh. CBMS wants "2082.083".
export function fiscalYearBs(ad: Date): string {
  const b = adToBs(ad);
  const startYear = b.month >= 4 ? b.year : b.year - 1;
  return `${startYear}.${String((startYear + 1) % 1000).padStart(3, '0')}`;
}
